def named_greeting(func):
    def wrapper(*args, **kwargs):
        name = args[0]
        name = name.upper()
        if "city" in kwargs.keys():
            city = kwargs["city"]
            city = city.upper()
            Greeting = func(name, city=city)
        else:
            Greeting = func(name)
        return Greeting
    return wrapper

@named_greeting
def greeting(name):
    return f"Hi {name}"

@named_greeting
def greeting2(name, city="Berlin"):
    return f"Hi {name}, {city}"

@named_greeting
def square(num):
    return num + num

